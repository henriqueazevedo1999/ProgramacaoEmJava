package aula3.controller;

public class Contato 
{
    private String fone;
    private String email;

    public Contato()
    {
    }

    public String getFone()
    {
        return fone;
    }

    public void setFone(String fone) 
    {
        this.fone = fone;
    }

    public String getEmail()
    {
        return email;
    }

    public void setEmail(String email) 
    {
        this.email = email;
    }
}
